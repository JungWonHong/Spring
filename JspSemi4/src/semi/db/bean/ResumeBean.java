package semi.db.bean;

public class ResumeBean {
	private String r_id;
	private String r_company;
	private String r_content;
	private String r_file;
	private String r_certi;
	
	public String getR_id() {
		return r_id;
	}
	public void setR_id(String r_id) {
		this.r_id = r_id;
	}
	public String getR_company() {
		return r_company;
	}
	public void setR_company(String r_company) {
		this.r_company = r_company;
	}
	public String getR_content() {
		return r_content;
	}
	public void setR_content(String r_content) {
		this.r_content = r_content;
	}
	public String getR_file() {
		return r_file;
	}
	public void setR_file(String r_file) {
		this.r_file = r_file;
	}
	public String getR_certi() {
		return r_certi;
	}
	public void setR_certi(String r_certi) {
		this.r_certi = r_certi;
	}
	
}
