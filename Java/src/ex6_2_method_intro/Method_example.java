package ex6_2_method_intro;

public class Method_example {

	public static void main(String[] args) {
		
		System.out.println("******************************");
		System.out.println("------------------------------");
		System.out.println("Hello, Java");
		System.out.println("------------------------------");
		System.out.println("******************************");

		for (int i = 0; i < 30; i++)
			System.out.print("*");
		System.out.println();
		for (int i = 0; i < 30; i++)
			System.out.print("-");
		System.out.println();
		System.out.println("Hello, Java");
		
		for (int i = 0; i < 30; i++)
			System.out.print("-");
		System.out.println();
		for (int i = 0; i < 30; i++)
			System.out.print("*");
		System.out.println();
	}

}

